function ajustarValorCalorias(calorias, objetivo) {
    let resto = calorias % 100; // Obtén el residuo de la división entre 100
    if (objetivo = "volumen") {
        // Redondear hacia arriba
        calorias += (100 - resto);
    } else {
        // Redondear hacia abajo
        calorias -= resto;
    }
    return calorias;
}

